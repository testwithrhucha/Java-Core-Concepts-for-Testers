package com.variables_and_datatypes;

public class DemoDatatype {

	public static void main(String[] args) {
		// Numeric data types
		// primitive 
	//byte
		
		byte b=123,col=12;
		System.out.println(b+" "+col);
		
	//int
		
		int a=4567,num=86478;
		System.out.println(a+" "+num);
		
		
	// short
		
		short sh=2390,no=12345;
		System.out.println(sh+no);
		
	// long
		
		long l=36747847,p=-73674673;
		System.out.println(l*p);
		
		long c=2367389456l;// literal is needed after end of the value when it is more than 10 digits either add l or L	
		System.out.println(c);
	// float
		
		float number=2.456789f;// it is also need literal either add f or F
		
		System.out.println(number);
		
	// double
		
		double price=56.364734; // no need literal
		
		System.out.println(price);
		
	// char 
		
		char ch='b',grade='a';
		System.out.println(ch+" "+grade);
		
	// boolean
		
		boolean s= true;
		boolean n=false; //only accept true and false
		
		System.out.println(s+" "+n);
		
	// String ---> non primitive
		
		String fname="Rhucha";
		String lname="Chougale";
		System.out.println(fname+" "+lname);

		
		//hence primitive data types stores one value in variable 
		// non primitive data types stores multiple values in one variable;
		
		
		
		
		
		
		
		

	}

}
